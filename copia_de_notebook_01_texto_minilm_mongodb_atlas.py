"""## 17. Construcción de contexto base para el siguiente notebook

Este bloque deja lista una función simple para convertir resultados en contexto.
"""

def build_context_from_results(df_results: pd.DataFrame, max_chars: int = 2500) -> str:
    blocks = []
    current_len = 0

    if df_results is None or df_results.empty:
        return ""

    for i, row in df_results.iterrows():
        block = (
            f"[Fragmento {i+1}]\n"
            f"Documento: {row['titulo']}\n"
            f"Idioma: {row['idioma']}\n"
            f"Estrategia: {row['estrategia_chunking']}\n"
            f"Score: {float(row['score']):.4f}\n"
            f"Contenido:\n{row['texto_chunk']}"
        )

        if current_len + len(block) > max_chars:
            break

        blocks.append(block)
        current_len += len(block) + 2

    return "\n\n".join(blocks)

