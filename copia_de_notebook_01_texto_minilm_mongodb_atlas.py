"""## 9. Construcción del dataset de chunks

Transformamos cada documento en múltiples chunks y agregamos metadatos:
- `codigo_doc`
- `titulo`
- `idioma`
- `fuente`
- `chunk_id`
- `estrategia_chunking`
"""

def build_chunk_records(documents: List[Dict]) -> pd.DataFrame:
    rows = []
    for doc in documents:
        for strategy_name, chunk_fn in STRATEGIES.items():
            chunks = chunk_fn(doc["contenido_texto"])
            for idx, chunk_text in enumerate(chunks):
                rows.append({
                    "codigo_doc": doc["codigo_doc"],
                    "titulo": doc["titulo"],
                    "idioma": doc["idioma"],
                    "fecha": doc["fecha"],
                    "fuente": doc["fuente"],
                    "contenido_texto": doc["contenido_texto"],
                    "chunk_id": idx,
                    "estrategia_chunking": strategy_name,
                    "texto_chunk": chunk_text,
                    "n_chars": len(chunk_text),
                })
    return pd.DataFrame(rows)

