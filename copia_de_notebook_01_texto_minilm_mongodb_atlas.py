"""## 7. Funciones de chunking

Implementamos tres estrategias:

- `fixed`: tamaño fijo aproximado por palabras
- `sentence`: respeta oraciones
- `semantic`: agrupa oraciones cercanas semánticamente
"""

def normalize_spaces(text: str) -> str:
    return re.sub(r"\s+", " ", text).strip()


def fixed_size_chunking(text: str, chunk_size: int = 80, overlap: int = 15) -> List[str]:
    words = normalize_spaces(text).split()
    chunks = []
    start = 0
    while start < len(words):
        end = min(start + chunk_size, len(words))
        chunk = " ".join(words[start:end]).strip()
        if chunk:
            chunks.append(chunk)
        if end == len(words):
            break
        start = max(end - overlap, start + 1)
    return chunks


def sentence_aware_chunking(text: str, max_sentences: int = 3, overlap_sentences: int = 1) -> List[str]:
    sentences = [s.strip() for s in sent_tokenize(text) if s.strip()]
    print(sentences)
    chunks = []
    start = 0
    while start < len(sentences):
        end = min(start + max_sentences, len(sentences))
        chunk = " ".join(sentences[start:end]).strip()
        if chunk:
            chunks.append(chunk)
        if end == len(sentences):
            break
        start = max(end - overlap_sentences, start + 1)
    return chunks


def semantic_chunking(
    text: str,
    threshold: float = 0.78,
    min_sentences: int = 1,
    max_sentences: int = 4
) -> List[str]:
    sentences = [s.strip() for s in sent_tokenize(text) if s.strip()]
    if not sentences:
        return []

    if len(sentences) == 1:
        return sentences

    sent_embeddings = embedding_model.encode(sentences, normalize_embeddings=True)
    chunks = []
    current_chunk = [sentences[0]]

    for i in range(1, len(sentences)):
        sim = float(np.dot(sent_embeddings[i-1], sent_embeddings[i]))
        if sim >= threshold and len(current_chunk) < max_sentences:
            current_chunk.append(sentences[i])
        else:
            if len(current_chunk) >= min_sentences:
                chunks.append(" ".join(current_chunk))
            current_chunk = [sentences[i]]

    if current_chunk:
        chunks.append(" ".join(current_chunk))

    return chunks


