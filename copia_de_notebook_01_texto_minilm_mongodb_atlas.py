"""## 12. Carga en MongoDB Atlas

Usamos `upsert` por combinación de:
- `codigo_doc`
- `estrategia_chunking`
- `chunk_id`

Así puedes reejecutar el notebook sin duplicar chunks.
"""

def load_chunks_to_mongodb(collection, docs: List[Dict]):
    ops = []
    for doc in docs:
        filt = {
            "codigo_doc": doc["codigo_doc"],
            "estrategia_chunking": doc["estrategia_chunking"],
            "chunk_id": doc["chunk_id"],
        }
        ops.append(
            UpdateOne(
                filt,
                {"$set": doc},
                upsert=True
            )
        )

    if not ops:
        return None

    result = collection.bulk_write(ops, ordered=False)
    return result

